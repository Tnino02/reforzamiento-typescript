// Ejercicio 1: Variables y Alcance

// Declarar una variable constante para el nombre del estudiante
const nombreEstudiante: string = "Tania Niño";

// Declarar una variable let para la edad
let edad: number = 21;

// Mostrar valores iniciales
console.log(`Nombre: ${nombreEstudiante}`);
console.log(`Edad inicial: ${edad}`);

// Actualizar el valor de la variable edad
edad = 22;
console.log(`Edad actualizada: ${edad}`);

/*
Explicación:
- const se usa para valores que no deben cambiar (como el nombre).
- let permite reasignar el valor (como la edad).
*/

